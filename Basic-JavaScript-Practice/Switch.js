const day = 'monday';

switch (day) {
    case 'monday'://day === 'monday'
        console.log('plan to study(mon)');
        break;
    case 'tuesday':
        console.log('plan to study(tue)');
        break;
    case 'wednesday':
        console.log('plan to study(wed)');
        break;
    case 'thursday':
        console.log('plan to study(thu)');
        break;
    case 'friday':
        console.log('plan to study(fri)');
        break;
    case 'saturday':
        console.log('plan to study(sat)');
        break;
    case 'sunday':
        console.log('plan to study(sun)');
        break;
    default:
        console.log('not a valid day');
}

