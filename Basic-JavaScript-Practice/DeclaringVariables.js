//values that can be change later are declared this way using LET
//block scoped
let age = 21;
age = 31;

//values that can not be change later are declared this way using CONST
const birthYear = 2002;
//we can not reassign any value to birthYear
//when using const we need initial value 

//old way of declaring variable similar to let 
//function scoped
var job = 'nothing';
job = 'freelancer';

